// Dicionário

var fruta = {nome:"maçã", cor:"vermelha"};
console.log(fruta); // Exibe as informações do array no dicionario
console.log(fruta.nome); //Exibe somente o elemento array selecionado
console.log(fruta.cor); // Exibe somente o elemento array selecionado
alert(fruta.nome + " " + fruta.cor); //Concatena os elementos do dicionário

